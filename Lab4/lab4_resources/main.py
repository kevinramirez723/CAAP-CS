#creates a turtle (pen) and sets the speed (where 0 is fastest and 10 is slowest).
#The colors can be set through their names or through hexadecimal codes, use hex for accuracy.
def initialize(speed):
	myPen = turtle.Turtle()	
	turtle.tracer(speed)	#How quickly the turtle drawing updates (Larger values are faster). Entering 0 finishes the drawing instantly.
	myPen.speed(10)			#myPen.setheading(n) points pen to given angle direction.
	myPen.color("#000000")	#0 points to the right, 90 to go up, 180 to go to the left 270 to go down.
	myPen.penup()			#Positions myPen in top left area of the screen.
	myPen.forward(-100)		#This should change according to the image size you want to make and the size of your boxes ("pixels").
	myPen.setheading(90)	#This canvas is currently set to 200*200 pixels or a 20x20 box of 10 sq pixels each.
	myPen.forward(100)
	myPen.setheading(0)
	return myPen

#This function draws a box by drawing each side of the square and using the fill function.
def box(myPen, boxsize):
	myPen.begin_fill()
	myPen.forward(boxsize)	#0 deg.
	myPen.left(90)
	myPen.forward(boxsize)	#90 deg.
	myPen.left(90)
	myPen.forward(boxsize)	#180 deg.
	myPen.left(90)
	myPen.forward(boxsize)	#270 deg.
	myPen.end_fill()
	myPen.setheading(0)
	myPen.forward(boxsize) #Moves right a box width to being the next square.

#Skips drawing anything by lifting pen up and moving over a box width and setting pen down again.
def skip(myPen):
	myPen.penup()
	myPen.forward(boxsize)
	myPen.pendown()

#Moves down to the next line when the pen finishes drawing a layer.
def shift_down(myPen, pixels):
	myPen.penup()
	myPen.forward(-len(pixels[1])*boxsize)
	myPen.right(90)
	myPen.forward(boxsize)
	myPen.left(90) 
	myPen.pendown()

