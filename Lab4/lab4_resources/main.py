#This function takes a pallet and pixel list to draw the picture. 
def draw(myPen, pallet, pixels):
	for i in range(len(pixels)):					#Outer loop keeps track of how many layers it has to draw before terminating.
		for j in range(len(pixels[i])):				#Inner loop checks the length of each inner list of pixels to know when its done with the layer and shift to the next one.
			if pallet[pixels[i][j]] == "#FFFFFF":	#The program will skip drawing any white pixels since the background is also white.
				skip(myPen)
			else:
				myPen.color(pallet[pixels[i][j]])	#Changes the color to be used for drawing the pixel based on the value within every pixels list element.
				box(myPen, boxsize)					#Draws the pixel.
		shift_down(myPen, pixels)

